package ex_04_switch_statements;

public class Switch_Qn3 {
    public static void main(String[] args) {
        int a =9;
        switch (a) {
            default:
                System.out.println("Default");
                break;
            case -1:
                System.out.println("109");
                break;
            case 12:
                System.out.println("9");
                break;
            case 17:
                System.out.println("9");
                break;
            case 19:
                System.out.println("9");
                break;
        }
    }
}

