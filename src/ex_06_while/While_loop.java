package ex_06_while;

public class While_loop {
    public static void main(String[] args) {
        int i = 0;
        while (i<10){
            System.out.println(i);
            i++;
        }

        System.out.println("---------------" );

        short ithan = 2;
        while (ithan <19){
            System.out.println(ithan);
            ithan++;
        }
    }
}
