package ex_06_while;

public class While_loop_Qn2 {
    public static void main(String[] args) {
        int i = 0;
        while (i > 0) {

            System.out.println("Enter");
            System.out.println(i);
            i++;
        }

        while (true){
            System.out.println("Hello, Pramod");
            break;
        }

        int i1 = 10;
        while (i1>=1){
            System.out.println(i1);
            i1--;
        }
    }
}
