package ex_05_for_loop;

public class ForLoop {
    public static void main(String[] args) {

        // for lop is used to repeat the block of code

        for (int i = 0 ;i <= 10; i ++ ){
            System.out.println(i);
        }

        System.out.println("-----------");

        for (byte naveen = 10 ; naveen > 0; naveen --){
            System.out.println(naveen);
        }
        System.out.println("-----------");
        for (int _1 = 0; _1 <= 10; _1++) { // 11 times
            System.out.println(_1); // 0
        }
    }
}
