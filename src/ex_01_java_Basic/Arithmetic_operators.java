package ex_01_java_Basic;

public class Arithmetic_operators {
    public static void main(String[] args) {
        int a = 20;
        int b = 3;
        float c = 3.0f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c); // 6.666665

        System.out.println("a+b");
        System.out.println(a+b);
    }
}
